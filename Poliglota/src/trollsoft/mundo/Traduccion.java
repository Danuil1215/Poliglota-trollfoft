package trollsoft.mundo;

/**
 * @author TrollSoft Team
 *
 */
public class Traduccion {

	private String nombre;
	private String idioma;
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @return the idioma
	 */
	public String getIdioma() {
		return idioma;
	}
	/**
	 * @param nombre
	 * @param idioma
	 */
	public Traduccion(String nombre, String idioma) {
		super();
		this.nombre = nombre;
		this.idioma = idioma;
	}
	
	
}
